const TitleListSorted = (list, ascending = true) => {
  
  if (list.length <= 1) return list;
  const findDif = (tid) => {
    return DifTable.find((obj) => obj.TitleId === tid).Difficulty;
  };
  list.sort((t1, t2) => {
    if (ascending) return findDif(t1) - findDif(t2);
    else return findDif(t2) - findDif(t1);
  });

  return list;
};
